//  glyph should reach out far enough on the positive X axis to encroach on the tag values (e.g. SPLINE).
//
//  Each glyph begins with a character value, followed by five extra items of data as follows:
//    * section separator (low)
//    * section seaprator (high)
//    * Y multiplier        (typically =  1)
