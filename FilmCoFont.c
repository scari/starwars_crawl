//         the processing of a glyph that consists of non-overlapping loops, such as the exclamation point (“!”).  (And, for optimal processing speed, the loop
//         with the larger area-to-corners ratio should be defined first.)
//
//  Note:  Straight quotes are not included in this font -- they are converted to directional quotes by the code that uses it.
//
//  Important:  All character values less than 256 are automatically converted to their Unicode values on app launch via the call to processFilmCoFont -- unless they
//              are negative, in which case they are made positive but otherwise not changed.



